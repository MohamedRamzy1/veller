     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            "applicant_id" => "required",
            "interests" => "required"
        ]);

        $model = new Model("interests");
        $requestData = $request->all();
        $model->insert($requestData);
